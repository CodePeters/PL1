public class rectangular{

    private int height,x,y,z;

    public rectangular (int height1,int z1,int y1,int x1){
        height=height1;
        x=x1;
        y=y1;
        z=z1;

    }

    public int GiveHeight (){
        return height;
    }

    public int GiveX (){
        return x;
    }

    public int GiveY (){
        return y;
    }

    public int GiveZ (){
        return z;
    }

}